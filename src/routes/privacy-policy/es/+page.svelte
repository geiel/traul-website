	<h3>8. CONTROLES PARA LAS FUNCIONES DE NO SEGUIMIENTO</h3>
	<p>
		La mayoría de los navegadores web y algunos sistemas operativos y aplicaciones móviles incluyen
		una función de No Seguimiento ("DNT") que puede activar para indicar su preferencia de
		privacidad de no tener datos sobre sus actividades de navegación en línea monitoreados y
		recopilados. En esta etapa, no se ha finalizado un estándar de tecnología uniforme para
		reconocer e implementar señales DNT. Como tal, actualmente no respondemos a las señales del
		navegador DNT ni a ningún otro mecanismo que comunique automáticamente su elección de no ser
		rastreado en línea. Si se adopta un estándar para el seguimiento en línea que debemos seguir en
		el futuro, le informaremos sobre esa práctica en una versión revisada de este aviso de
		privacidad.
	</p>

